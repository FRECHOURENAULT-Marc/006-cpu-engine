#pragma once

struct cpu_particle_physics
{
	float gx;
	float gy;
	float gz;
	float drag;					// 0.. (ex: 1..5)
	float maxSpeed;				// 0 = no clamp

	float minX;
	float minY;
	float minZ;
	float maxX;
	float maxY;
	float maxZ;
	bool useBounds;
	float bounce;				// 0..1

	cpu_particle_physics();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct cpu_particle_data
{
	int maxCount = 0;
	int alive = 0;

	float* px = nullptr;
	float* py = nullptr;
	float* pz = nullptr;

	float* vx = nullptr;
	float* vy = nullptr;
	float* vz = nullptr;

	float* age  = nullptr;
	float* duration = nullptr;
	ui32* seed = nullptr;

	float* r = nullptr;
	float* g = nullptr;
	float* b = nullptr;

	cpu_particle_data();
	~cpu_particle_data();

	void Create(int maxP);
	void Destroy();
	void Update(float dt, const cpu_particle_physics& phys);
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct cpu_particle_emitter
{
	int index;
	int sortedIndex;
	bool dead;

	float rate; // particles/sec
	float accum;
	float durationMin;
	float durationMax;

	cpu_particle_emitter();
	void Update(cpu_particle_data& p, float dt, float ox, float oy, float oz, float baseVx, float baseVy, float baseVz, float r, float g, float b);
};
