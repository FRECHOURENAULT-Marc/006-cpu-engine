#pragma once

struct cpu_particle_physics
{
	float gx;
	float gy;
	float gz;
	float drag;					// 0.. (ex: 1..5)
	float maxSpeed;				// 0 = no clamp

	float minX;
	float minY;
	float minZ;
	float maxX;
	float maxY;
	float maxZ;
	bool useBounds;
	float bounce;				// 0..1

	cpu_particle_physics();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct cpu_particle_data
{
	int maxCount;
	int alive;

	void* blob;
	int size;

	float* px;
	float* py;
	float* pz;

	float* vx;
	float* vy;
	float* vz;

	float* age;
	float* duration;
	float* invDuration;
	ui32* seed;

	float* r;
	float* g;
	float* b;

	cpu_particle_data();
	~cpu_particle_data();
	void Reset();

	void Create(int maxP);
	void Destroy();
	void Update(float dt, const cpu_particle_physics& phys);
	inline void ApplyBounds(float& px, float& py, float& pz, float& vx, float& vy, float& vz, const cpu_particle_physics& phys);
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct cpu_particle_emitter
{
	int index;
	int sortedIndex;
	bool dead;

	float rate; // particles/sec
	float durationMin;
	float durationMax;
	XMFLOAT3 pos;
	XMFLOAT3 dir;
	XMFLOAT3 color;
	float speed;

private:
	float accum;

public:
	cpu_particle_emitter();

	void Update(cpu_particle_data& p, float dt);
};
